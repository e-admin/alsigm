package ieci.tecdoc.sgm.rpadmin.beans;

public class SicresOficinaInformeImpl {
	//Campo de la Tabla
	private int id;
	private int idReport;
	private int idOfic;
	
	//Campos Axiliares
	private String nameOfic;
	private String codeOfic;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the idReport
	 */
	public int getIdReport() {
		return idReport;
	}
	/**
	 * @param idReport the idReport to set
	 */
	public void setIdReport(int idReport) {
		this.idReport = idReport;
	}
	/**
	 * @return the idOfic
	 */
	public int getIdOfic() {
		return idOfic;
	}
	/**
	 * @param idOfic the idOfic to set
	 */
	public void setIdOfic(int idOfic) {
		this.idOfic = idOfic;
	}
	/**
	 * @return the nameOfic
	 */
	public String getNameOfic() {
		return nameOfic;
	}
	/**
	 * @param nameOfic the nameOfic to set
	 */
	public void setNameOfic(String nameOfic) {
		this.nameOfic = nameOfic;
	}
	/**
	 * @return the codeOfic
	 */
	public String getCodeOfic() {
		return codeOfic;
	}
	/**
	 * @param codeOfic the codeOfic to set
	 */
	public void setCodeOfic(String codeOfic) {
		this.codeOfic = codeOfic;
	}
	
	
}
